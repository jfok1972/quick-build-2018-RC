package com.jhopesoft.framework.exception;


public class PreglacialDateParseException extends PreglacialException {
	private static final long serialVersionUID = 1L;
	
	
	public PreglacialDateParseException() {
		super();
	}
	
	public PreglacialDateParseException(String message) {
		super(message);
	}
	
	public PreglacialDateParseException(Throwable cause) {
		super(cause);
	}
	
	public PreglacialDateParseException(String message, Throwable cause) {
		super(message,cause);
	}
	
	

}
