package com.jhopesoft.framework.exception;


public class PreglacialSqlParseException extends PreglacialException {
	private static final long serialVersionUID = 1L;
	
	
	public PreglacialSqlParseException() {
		super();
	}
	
	public PreglacialSqlParseException(String message) {
		super(message);
	}
	
	public PreglacialSqlParseException(Throwable cause) {
		super(cause);
	}
	
	public PreglacialSqlParseException(String message, Throwable cause) {
		super(message,cause);
	}
	
	

}
